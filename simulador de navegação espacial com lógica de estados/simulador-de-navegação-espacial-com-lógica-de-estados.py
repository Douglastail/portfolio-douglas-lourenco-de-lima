def simulador_evacuacao():



  locais = ["Quarto", "Cozinha", "Sala", "Porta", "Portão"]



  estados = [

    "caminho livre",

    "caminho livre",

    "tem chave",

    "porta trancada",

    "saida"

  ]



  posicao = 0

  tem_chave = False

  energia = 10



  print("INICIANDO EVACUACAO...\n")



  while energia > 0:



    print("Local atual:", locais[posicao])



    estado = estados[posicao]



    if estado == "caminho livre":

      print("Caminho livre, avançando...")

      posicao = posicao + 1



    elif estado == "tem chave":

      print("Voce encontrou a chave na sala!")

      tem_chave = True

      posicao = posicao + 1



    elif estado == "porta trancada":

      print("Porta trancada!")





      if tem_chave == True:

        print("Usou a chave e abriu a porta!")

        posicao = posicao + 1

      else:

        print("Sem chave, voltando...")

        posicao = posicao - 1



    elif estado == "saida":

      print("Voce saiu com sucesso!")

      break



    energia = energia - 1

    print("Energia restante:", energia, "\n")



  if energia == 0:

    print("O agente ficou sem energia!")





