def simulador_evacuacao_interativo():
    # Mapeamento do ambiente
    locais = ["Quarto", "Cozinha", "Sala", "Porta", "Portão"]

    # Estados de cada local
    estados = [
        "caminho livre", # Quarto
        "caminho livre", # Cozinha
        "tem chave",     # Sala
        "porta trancada",# Porta
        "saida"          # Portão
    ]

    posicao = 0
    tem_chave = False
    energia = 15 # Aumentei a energia para dar tempo de explorar

    print("=== INTERFACE DE EVACUAÇÃO INTERATIVA ===")
    print("Comandos: [f] Frente | [t] Trás\n")

    while energia > 0:
        print(f"📍 Local atual: {locais[posicao]}")
        print(f"⚡ Energia: {energia}")

        # Verifica se encontrou a chave automaticamente ao chegar na Sala
        if estados[posicao] == "tem chave" and not tem_chave:
            print("🔑 Você encontrou a chave na Sala!")
            tem_chave = True

        # Verifica se chegou na saída
        if estados[posicao] == "saida":
            print("\n🏁 PARABÉNS! Você alcançou o portão e saiu com sucesso!")
            break

        # Pergunta a direção ao usuário
        direcao = input("Para onde deseja ir? (f/t): ").lower()

        if direcao == 'f':
            # Tenta avançar
            nova_posicao = posicao + 1

            # Bloqueio de limite do mapa
            if nova_posicao >= len(locais):
                print("❌ Não há nada além deste ponto.")
            # Bloqueio da Porta Trancada
            elif estados[nova_posicao] == "porta trancada" and not tem_chave:
                print("🔒 A porta está trancada! Você precisa da chave da Sala.")
            else:
                posicao = nova_posicao
                print("🏃 Avançando...")

        elif direcao == 't':
            # Tenta voltar
            if posicao > 0:
                posicao -= 1
                print("⬅️ Voltando...")
            else:
                print("❌ Você já está no início (Quarto).")

        else:
            print("⚠️ Comando inválido! Use 'f' para frente ou 't' para trás.")

        energia -= 1
        print("-" * 30)

    if energia == 0:
        print("\n💀 O agente ficou sem energia e não conseguiu sair!")

# Executar o novo simulador
simulador_evacuacao_interativo()

